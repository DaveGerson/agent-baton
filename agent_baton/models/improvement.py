# TriggerConfig
# ---------------------------------------------------------------------------

@dataclass
class TriggerConfig:
    """Configuration for when to trigger improvement analysis.

    Controls the cadence and sensitivity of the closed-loop improvement
    system.  Thresholds are compared against rolling-window metrics
    computed from usage logs.

    Attributes:
        min_tasks_before_analysis: Minimum completed tasks before the
            first analysis runs.
        analysis_interval_tasks: Re-analyze every N new tasks.
        agent_failure_threshold: Agent failure rate that triggers an anomaly.
        gate_failure_threshold: Gate failure rate that triggers an anomaly.
        budget_deviation_threshold: Fractional deviation from budget tier
            that triggers a recommendation.
        confidence_threshold: Minimum confidence for auto-applying a
            recommendation.
        learning_cycle_count_threshold: Number of completed executions since
            the last learning cycle before flagging that a cycle is due.
            Controlled by the ``LEARNING_TRIGGER_COUNT`` env var.
            Default 10.  Set to 0 to disable the counter-based flag.
    """

    min_tasks_before_analysis: int = 3
    analysis_interval_tasks: int = 3
    agent_failure_threshold: float = 0.3
    gate_failure_threshold: float = 0.2
    budget_deviation_threshold: float = 0.5
    confidence_threshold: float = 0.7
    learning_cycle_count_threshold: int = 10

    def to_dict(self) -> dict:
        return {
            "min_tasks_before_analysis": self.min_tasks_before_analysis,
            "analysis_interval_tasks": self.analysis_interval_tasks,
            "agent_failure_threshold": self.agent_failure_threshold,
            "gate_failure_threshold": self.gate_failure_threshold,
            "budget_deviation_threshold": self.budget_deviation_threshold,
            "confidence_threshold": self.confidence_threshold,
            "learning_cycle_count_threshold": self.learning_cycle_count_threshold,
        }

    @classmethod
    def from_dict(cls, data: dict) -> TriggerConfig:
        return cls(
            min_tasks_before_analysis=int(data.get("min_tasks_before_analysis", 3)),
            analysis_interval_tasks=int(data.get("analysis_interval_tasks", 3)),
            agent_failure_threshold=float(data.get("agent_failure_threshold", 0.3)),
            gate_failure_threshold=float(data.get("gate_failure_threshold", 0.2)),
            budget_deviation_threshold=float(data.get("budget_deviation_threshold", 0.5)),
            confidence_threshold=float(data.get("confidence_threshold", 0.7)),
            learning_cycle_count_threshold=int(
                data.get("learning_cycle_count_threshold", 10)
            ),
        )

    @classmethod
    def from_env(cls) -> TriggerConfig:
        """Build a :class:`TriggerConfig` with values from environment variables.

        Reads the following env vars (all optional; falls back to the class
        defaults when absent or non-numeric):

        * ``BATON_MIN_TASKS`` — ``min_tasks_before_analysis`` (int, default 3).
        * ``BATON_ANALYSIS_INTERVAL`` — ``analysis_interval_tasks`` (int, default 3).
        * ``LEARNING_TRIGGER_COUNT`` — ``learning_cycle_count_threshold`` (int,
          default 10).  Set to 0 to disable the counter-based learning cycle flag.

        Other thresholds (failure rates, budget deviation, confidence) are not
        overridable via env vars because they are anomaly-detection knobs rather
        than cadence controls, and mis-setting them via env can silently suppress
        important signals.

        Returns:
            A :class:`TriggerConfig` populated from env vars where present.
        """
        def _int(key: str, default: int) -> int:
            raw = os.environ.get(key, "").strip()
            if raw:
                try:
                    val = int(raw)
                    if val >= 0:
                        return val
                except ValueError:
                    pass
            return default

        return cls(
            min_tasks_before_analysis=_int("BATON_MIN_TASKS", 3),
            analysis_interval_tasks=_int("BATON_ANALYSIS_INTERVAL", 3),
            learning_cycle_count_threshold=_int("LEARNING_TRIGGER_COUNT", 10),
        )


