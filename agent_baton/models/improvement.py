# Anomaly
# ---------------------------------------------------------------------------

@dataclass
class Anomaly:
    """A detected anomaly in agent or system behavior.

    Anomalies are surfaced by the improvement analysis pipeline when a
    metric exceeds its configured threshold.  They may trigger
    ``Recommendation`` generation or be reported for manual review.

    Attributes:
        anomaly_type: Classification of the anomaly (e.g.
            ``"high_failure_rate"``, ``"budget_overrun"``, ``"retry_spike"``).
        severity: Impact level (``AnomalySeverity`` value).
        agent_name: Affected agent, if agent-specific.
        metric: The metric that triggered detection.
        current_value: Observed metric value.
        threshold: Threshold that was exceeded.
        sample_size: Number of data points in the detection window.
        evidence: References to supporting data (task IDs, log entries).
    """

    anomaly_type: str           # e.g. "high_failure_rate", "budget_overrun", "retry_spike"
    severity: str               # AnomalySeverity value
    agent_name: str = ""
    metric: str = ""
    current_value: float = 0.0
    threshold: float = 0.0
    sample_size: int = 0
    evidence: list[str] = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "anomaly_type": self.anomaly_type,
            "severity": self.severity,
            "agent_name": self.agent_name,
            "metric": self.metric,
            "current_value": self.current_value,
            "threshold": self.threshold,
            "sample_size": self.sample_size,
            "evidence": self.evidence,
        }

    @classmethod
    def from_dict(cls, data: dict) -> Anomaly:
        return cls(
            anomaly_type=data.get("anomaly_type", ""),
            severity=data.get("severity", "low"),
            agent_name=data.get("agent_name", ""),
            metric=data.get("metric", ""),
            current_value=float(data.get("current_value", 0.0)),
            threshold=float(data.get("threshold", 0.0)),
            sample_size=int(data.get("sample_size", 0)),
            evidence=data.get("evidence", []),
        )


