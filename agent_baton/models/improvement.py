@dataclass
class ImprovementReport:
    """Summary of a single improvement analysis cycle.

    Produced after each analysis run and persisted to the improvement
    log.  Contains the anomalies detected, recommendations generated,
    and actions taken (auto-applied or escalated for review).

    Attributes:
        report_id: Unique report identifier.
        timestamp: ISO 8601 timestamp of the analysis run.
        skipped: ``True`` if the analysis was skipped (e.g. insufficient data).
        reason: Explanation for why the analysis was skipped.
        anomalies: Serialized ``Anomaly`` instances found.
        recommendations: Serialized ``Recommendation`` instances generated.
        auto_applied: ``rec_id`` values of recommendations that were
            automatically applied.
        escalated: ``rec_id`` values of recommendations escalated for
            human review.
        active_experiments: ``experiment_id`` values of experiments
            currently running.
    """

    report_id: str
    timestamp: str = ""
    skipped: bool = False
    reason: str = ""            # why skipped, if applicable
    anomalies: list[dict] = field(default_factory=list)     # list of Anomaly.to_dict()
    recommendations: list[dict] = field(default_factory=list)  # list of Recommendation.to_dict()
    auto_applied: list[str] = field(default_factory=list)   # rec_ids that were auto-applied
    escalated: list[str] = field(default_factory=list)      # rec_ids that were escalated
    active_experiments: list[str] = field(default_factory=list)  # experiment_ids

    def __post_init__(self) -> None:
        if not self.timestamp:
            self.timestamp = datetime.now(timezone.utc).isoformat(timespec="seconds")

    def to_dict(self) -> dict:
        return {
            "report_id": self.report_id,
            "timestamp": self.timestamp,
            "skipped": self.skipped,
            "reason": self.reason,
            "anomalies": self.anomalies,
            "recommendations": self.recommendations,
            "auto_applied": self.auto_applied,
            "escalated": self.escalated,
            "active_experiments": self.active_experiments,
        }

    @classmethod
    def from_dict(cls, data: dict) -> ImprovementReport:
        return cls(
            report_id=data["report_id"],
            timestamp=data.get("timestamp", ""),
            skipped=bool(data.get("skipped", False)),
            reason=data.get("reason", ""),
            anomalies=data.get("anomalies", []),
            recommendations=data.get("recommendations", []),
            auto_applied=data.get("auto_applied", []),
            escalated=data.get("escalated", []),
            active_experiments=data.get("active_experiments", []),
        )


# ---------------------------------------------------------------------------
# ImprovementConfig
# ---------------------------------------------------------------------------

@dataclass
class ImprovementConfig:
    """Top-level configuration for the closed-loop improvement system.

    Attributes:
        auto_apply_threshold: Minimum confidence score for a
            recommendation to be applied without human approval.
        paused: Circuit breaker — when ``True``, no recommendations
            are applied regardless of confidence.
    """

    auto_apply_threshold: float = 0.8
    paused: bool = False        # circuit breaker state

    def to_dict(self) -> dict:
        return {
            "auto_apply_threshold": self.auto_apply_threshold,
            "paused": self.paused,
        }

    @classmethod
    def from_dict(cls, data: dict) -> ImprovementConfig:
        return cls(
            auto_apply_threshold=float(data.get("auto_apply_threshold", 0.8)),
            paused=bool(data.get("paused", False)),
        )
