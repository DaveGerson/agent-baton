"""Data models for the closed-loop improvement system.

Covers recommendations, experiments, anomalies, trigger configuration,
improvement reports, and top-level improvement configuration.
"""
from __future__ import annotations

import os
from dataclasses import dataclass, field
from datetime import datetime, timezone
from enum import Enum


# ---------------------------------------------------------------------------
# Enums
# ---------------------------------------------------------------------------

class RecommendationCategory(Enum):
    """What aspect of the system a recommendation targets."""

    AGENT_PROMPT = "agent_prompt"
    BUDGET_TIER = "budget_tier"
    ROUTING = "routing"
    SEQUENCING = "sequencing"
    GATE_CONFIG = "gate_config"
    ROSTER = "roster"


class RecommendationStatus(Enum):
    """Lifecycle state of an improvement recommendation."""

    PROPOSED = "proposed"
    APPLIED = "applied"
    REJECTED = "rejected"
    ROLLED_BACK = "rolled_back"


class ExperimentStatus(Enum):
    """Lifecycle state of an A/B experiment tracking a recommendation."""

    RUNNING = "running"
    CONCLUDED = "concluded"
    ROLLED_BACK = "rolled_back"


class AnomalySeverity(Enum):
    """Severity tier for detected behavioral anomalies."""

    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"


# ---------------------------------------------------------------------------
# Recommendation
# ---------------------------------------------------------------------------

@dataclass
class Recommendation:
    """A single improvement recommendation produced by the analysis pipeline.

    Recommendations are generated by the closed-loop improvement system
    after analyzing usage logs, retrospectives, and scoring data.  Low-risk
    recommendations with confidence above the auto-apply threshold are
    applied automatically; others are surfaced for human review.

    Attributes:
        rec_id: Unique recommendation identifier.
        category: Area of the system to change (``RecommendationCategory`` value).
        target: Specific entity to modify (e.g. agent name, task type).
        action: Short verb phrase describing the change.
        description: Human-readable explanation of the recommendation.
        evidence: References to data that supports this recommendation.
        confidence: Confidence score in ``[0.0, 1.0]``.
        risk: Assessed risk of applying the change.
        auto_applicable: Whether this can be applied without human approval.
        proposed_change: Machine-readable specification of the change.
        rollback_spec: Instructions for reverting the change if needed.
        created_at: ISO 8601 creation timestamp.
        status: Current lifecycle state (``RecommendationStatus`` value).
    """

    rec_id: str
    category: str               # RecommendationCategory value
    target: str                 # e.g. agent name, task type, gate name
    action: str                 # short verb phrase: "downgrade budget", "adjust routing"
    description: str            # human-readable explanation
    evidence: list[str] = field(default_factory=list)   # supporting data references
    confidence: float = 0.0     # 0.0 - 1.0
    risk: str = "low"           # "low", "medium", "high"
    auto_applicable: bool = False
    proposed_change: dict = field(default_factory=dict)  # machine-readable change spec
    rollback_spec: dict = field(default_factory=dict)    # how to undo
    created_at: str = ""
    status: str = "proposed"    # RecommendationStatus value

    def __post_init__(self) -> None:
        if not self.created_at:
            self.created_at = datetime.now(timezone.utc).isoformat(timespec="seconds")

    def to_dict(self) -> dict:
        return {
            "rec_id": self.rec_id,
            "category": self.category,
            "target": self.target,
            "action": self.action,
            "description": self.description,
            "evidence": self.evidence,
            "confidence": self.confidence,
            "risk": self.risk,
            "auto_applicable": self.auto_applicable,
            "proposed_change": self.proposed_change,
            "rollback_spec": self.rollback_spec,
            "created_at": self.created_at,
            "status": self.status,
        }

    @classmethod
    def from_dict(cls, data: dict) -> Recommendation:
        return cls(
            rec_id=data["rec_id"],
            category=data.get("category", ""),
            target=data.get("target", ""),
            action=data.get("action", ""),
            description=data.get("description", ""),
            evidence=data.get("evidence", []),
            confidence=float(data.get("confidence", 0.0)),
            risk=data.get("risk", "low"),
            auto_applicable=bool(data.get("auto_applicable", False)),
            proposed_change=data.get("proposed_change", {}),
            rollback_spec=data.get("rollback_spec", {}),
            created_at=data.get("created_at", ""),
            status=data.get("status", "proposed"),
        )


# ---------------------------------------------------------------------------
# Experiment
# ---------------------------------------------------------------------------

@dataclass
class Experiment:
    """Tracks the real-world outcome of an applied recommendation.

    After a recommendation is applied, an experiment monitors the
    relevant metric over subsequent executions.  Once enough samples
    are collected (or the time limit expires), the experiment concludes
    with a verdict that determines whether to keep or roll back the
    change.

    Attributes:
        experiment_id: Unique experiment identifier.
        recommendation_id: The ``Recommendation.rec_id`` being tested.
        hypothesis: What improvement is expected.
        metric: The metric being tracked (e.g. ``"first_pass_rate"``).
        baseline_value: Metric value before the change was applied.
        target_value: Metric value the change aims to achieve.
        agent_name: Agent under observation, if agent-specific.
        started_at: ISO 8601 timestamp of experiment start.
        min_samples: Minimum data points before concluding.
        max_duration_days: Auto-conclude after this many days.
        status: Lifecycle state (``ExperimentStatus`` value).
        samples: Collected metric values during the experiment.
        result: Verdict — ``"improved"``, ``"degraded"``,
            ``"inconclusive"``, or empty if still running.
    """

    experiment_id: str
    recommendation_id: str
    hypothesis: str
    metric: str                     # e.g. "first_pass_rate", "gate_pass_rate"
    baseline_value: float = 0.0
    target_value: float = 0.0
    agent_name: str = ""
    started_at: str = ""
    min_samples: int = 5
    max_duration_days: int = 14
    status: str = "running"         # ExperimentStatus value
    samples: list[float] = field(default_factory=list)
    result: str = ""                # "improved", "degraded", "inconclusive", ""

    def __post_init__(self) -> None:
        if not self.started_at:
            self.started_at = datetime.now(timezone.utc).isoformat(timespec="seconds")

    def to_dict(self) -> dict:
        return {
            "experiment_id": self.experiment_id,
            "recommendation_id": self.recommendation_id,
            "hypothesis": self.hypothesis,
            "metric": self.metric,
            "baseline_value": self.baseline_value,
            "target_value": self.target_value,
            "agent_name": self.agent_name,
            "started_at": self.started_at,
            "min_samples": self.min_samples,
            "max_duration_days": self.max_duration_days,
            "status": self.status,
            "samples": self.samples,
            "result": self.result,
        }

    @classmethod
    def from_dict(cls, data: dict) -> Experiment:
        return cls(
            experiment_id=data["experiment_id"],
            recommendation_id=data.get("recommendation_id", ""),
            hypothesis=data.get("hypothesis", ""),
            metric=data.get("metric", ""),
            baseline_value=float(data.get("baseline_value", 0.0)),
            target_value=float(data.get("target_value", 0.0)),
            agent_name=data.get("agent_name", ""),
            started_at=data.get("started_at", ""),
            min_samples=int(data.get("min_samples", 5)),
            max_duration_days=int(data.get("max_duration_days", 14)),
            status=data.get("status", "running"),
            samples=data.get("samples", []),
            result=data.get("result", ""),
        )


